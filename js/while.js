// This variable will generate a random number between 50 and 100
var allCones = Math.floor(Math.random() * 50) + 50;
// This expression will generate a random number between 1 and 5
var conesBought;

do {
    console.log("I have " + allCones + " left to sell today.");
    conesBought = Math.floor(Math.random() * 5) + 1
    if (conesBought > allCones) {
        console.log("Sorry, we only have " + allCones + " cones left. We can't give you " + conesBought);
    } else {
        allCones = allCones - conesBought;
        console.log("You have purchased " + conesBought + " cones. Enjoy!");
    }
    if (allCones === 0) {
        console.log("Hooray! We've sold all cones!");
    }
} while (allCones !== 0)